char	*ft_strcpy( char *dest, char *src )
{
	int		i;

	i = 0;
	while (dest[i])
	{
		dest[i] -= dest[i];
		i++;
	}
	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	return (dest);
}
